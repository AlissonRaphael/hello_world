#include <iostream>

int main() {
  std::cout<<"10 > 5: "<<(10>5)<<std::endl;
  std::cout<<"10 < 5: "<<(10<5)<<std::endl;
  std::cout<<"10 == 5: "<<(10==5)<<std::endl;
  std::cout<<"5 == 5: "<<(5==5)<<std::endl;
  std::cout<<"5 >= 5: "<<(5>=5)<<std::endl;
  std::cout<<"5 <= 10: "<<(5<=10)<<std::endl;

  return 0;
}
