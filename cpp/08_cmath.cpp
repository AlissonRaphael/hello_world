#include <iostream>
#include <cmath>

int main() {
  std::cout<<"Cosine 45: "<<cos(45)<<std::endl;
  std::cout<<"Sine 45: "<<sin(45)<<std::endl;

  std::cout<<"Power 2^3: "<<pow(2, 3)<<std::endl;
  std::cout<<"Square Root 144: "<<sqrt(144)<<std::endl;

  std::cout<<"Absolute -30: "<<abs(-30)<<std::endl;

  return 0;
}
